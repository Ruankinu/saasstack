export const generateAlerts = (subscriptions: Subscription[]): Alert[] => {
  const alerts: Alert[] = [];
  const now = new Date();

  subscriptions.forEach(sub => {
    if (sub.status === 'cancelled') return;

    const renewalDate = new Date(sub.renewalDate);
    const diffDays = Math.ceil((renewalDate.getTime() - now.getTime()) / (1000 * 60 * 60 * 24));

    if (diffDays <= 3 && diffDays > 0) {
      alerts.push({
        id: `alert-${sub.id}-3d`,
        organizationId: sub.organizationId,
        subscriptionId: sub.id,
        subscriptionName: sub.serviceName,
        type: 'renewal',
        priority: 'critical',
        title: `${sub.serviceName} renews in ${diffDays} day${diffDays === 1 ? '' : 's'}`,
        message: `Your ${sub.serviceName} (${sub.planName}) subscription renews on ${sub.renewalDate}. Total cost: $${(sub.price * sub.licenses).toFixed(2)}/mo.`,
        read: false,
        dismissed: false,
        createdAt: new Date().toISOString(),
        daysUntilRenewal: diffDays,
      });
    } else if (diffDays <= 7 && diffDays > 3) {
      alerts.push({
        id: `alert-${sub.id}-7d`,
        organizationId: sub.organizationId,
        subscriptionId: sub.id,
        subscriptionName: sub.serviceName,
        type: 'renewal',
        priority: 'high',
        title: `${sub.serviceName} renews in ${diffDays} days`,
        message: `Your ${sub.serviceName} (${sub.planName}) subscription renews on ${sub.renewalDate}.`,
        read: false,
        dismissed: false,
        createdAt: new Date().toISOString(),
        daysUntilRenewal: diffDays,
      });
    } else if (diffDays <= 15 && diffDays > 7) {
      alerts.push({
        id: `alert-${sub.id}-15d`,
        organizationId: sub.organizationId,
        subscriptionId: sub.id,
        subscriptionName: sub.serviceName,
        type: 'renewal',
        priority: 'medium',
        title: `${sub.serviceName} renews in ${diffDays} days`,
        message: `Your ${sub.serviceName} (${sub.planName}) subscription renews on ${sub.renewalDate}.`,
        read: true,
        dismissed: false,
        createdAt: daysAgo(2) + 'T10:00:00Z',
        daysUntilRenewal: diffDays,
      });
    } else if (diffDays <= 30 && diffDays > 15) {
      alerts.push({
        id: `alert-${sub.id}-30d`,
        organizationId: sub.organizationId,
        subscriptionId: sub.id,
        subscriptionName: sub.serviceName,
        type: 'renewal',
        priority: 'low',
        title: `${sub.serviceName} renews in ${diffDays} days`,
        message: `Your ${sub.serviceName} (${sub.planName}) subscription renews on ${sub.renewalDate}.`,
        read: true,
        dismissed: false,
        createdAt: daysAgo(5) + 'T10:00:00Z',
        daysUntilRenewal: diffDays,
      });
    }

    if (diffDays < 0 && sub.status === 'active') {
      alerts.push({
        id: `alert-${sub.id}-expired`,
        organizationId: sub.organizationId,
        subscriptionId: sub.id,
        subscriptionName: sub.serviceName,
        type: 'expired_active',
        priority: 'critical',
        title: `${sub.serviceName} may be expired`,
        message: `${sub.serviceName} renewal date was ${sub.renewalDate} but it's still marked as active. Please verify and update.`,
        read: false,
        dismissed: false,
        createdAt: new Date().toISOString(),
      });
    }
  });

  return alerts.sort((a, b) => {
    const priorityOrder = { critical: 0, high: 1, medium: 2, low: 3 };
    return priorityOrder[a.priority] - priorityOrder[b.priority];
  });
};

