export const seedPaymentHistory: PaymentHistory[] = (() => {
  const payments: PaymentHistory[] = [];
  const subs = seedSubscriptions.filter(s => s.status === 'active');
  const months = ['2024-07', '2024-08', '2024-09', '2024-10', '2024-11', '2024-12'];

  months.forEach(month => {
    subs.forEach(sub => {
      const startMonth = sub.startDate.substring(0, 7);
      if (month >= startMonth) {
        payments.push({
          id: `pay-${sub.id}-${month}`,
          subscriptionId: sub.id,
          subscriptionName: sub.serviceName,
          amount: sub.price * sub.licenses,
          date: `${month}-01`,
          status: 'paid',
        });
      }
    });
  });

  return payments;
})();

